void rshtDialog::validate() {
  dirty = false;  // assume nothing has changed
  
  /* determine minimum and maximum indices */
  int oldMin = minIndex;
  int oldMax = maxIndex;
  minIndex = ui->minBox->value();
  if (oldMin != minIndex)
    dirty = true;
  maxIndex = ui->maxBox->value();
  if (oldMax != maxIndex)
    dirty = true;

  /* determine iterations */
  int oldNoIterations = noIterations;
  noIterations = ui->iterationBox->value();
  if (oldNoIterations != noIterations)
    dirty = true;
  
  isConfigured = true;
  return;
}

