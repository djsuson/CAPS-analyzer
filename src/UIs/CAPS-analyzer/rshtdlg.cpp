void rshtDialog::finalize() {
  validate(); //check and store selections
  dataAssoc->transformationEngineType(Rsht);

  Q_EMIT transformData();
  accept();
}

void rshtDialog::help() {

}

void rshtDialog::cancel() {
  QString title, message;
//  QMessageBox::StandardButton reply;

  if (dirty) {
    title = QString(tr("Transformation parameters will be reset"));
    message = QString(tr("You have chosen to cancel after changing some parameters. These will be reset.\nThis will not affect any parameters stored in the transformation engine"));
    QMessageBox::information(this,title,message);
    reset();
    dirty = false;
  }
  Q_EMIT transformationCancelled();
  close();
}
