#include "pixelizer.h"

Pixelizer::Pixelizer() {
  m_configured = false;
  m_active     = false;
  m_average    = false;
  m_variance   = false;
  m_scheme     = NotPixelized;
}

Pixelizer::Pixelizer(Pixelizer* from) {
  m_configured = from->configured();
  m_active     = from->active();
  m_average    = from->average();
  m_variance   = from->variance();
  m_scheme     = from->pixelizerScheme();
}

Pixelizer::~Pixelizer() {
  clear();
}

void Pixelizer::clear() {
  m_active = false;
  m_configured = false;
}
